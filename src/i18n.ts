import { aiMessages } from './i18n.ai';

const base: Record<Lang, Dict> = { zh, en, ja, ko, hi, vi, th, de, es, fr };

// Merge the AI-feature strings into each language dictionary.
export const messages: Record<Lang, Dict> = Object.fromEntries(
  (Object.keys(base) as Lang[]).map((l) => [l, { ...base[l], ...aiMessages[l] }]),
) as Record<Lang, Dict>;

export function format(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
